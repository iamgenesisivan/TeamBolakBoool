         $("#attendanceChoice-s1 li").click(function(e) {
          var selKeyVal = $(this).attr("value");
            if(selKeyVal == "Absent"){
              $("#student1-att").css("background-color", "#dc3545");
              $("#student1-text").css("color", "white").text("Absent");
              e.preventDefault();
            }else if(selKeyVal == "Present"){
              $("#student1-att").css("background-color", "#28A745");
              $("#student1-text").css("color", "white").text("Present");
               e.preventDefault();
            }else if(selKeyVal == "Late"){
              $("#student1-att").css("background-color", "#FFC107");
              $("#student1-text").css("color", "white").text("Late");
               e.preventDefault();
            }else{
              $("#student1-att").css("background-color", "#17A2B8");
              $("#student1-text").css("color", "white").text("Excused");
               e.preventDefault();
            }
         });
