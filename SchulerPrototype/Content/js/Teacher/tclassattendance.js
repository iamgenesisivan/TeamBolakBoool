         $(document).ready(function(){
           var oTable = $('#stud-attendance').DataTable({
             "orderCellsTop": false,
             "searchHighlight" : true,
           });
           $('#search').keyup(function(){
                 oTable.search($(this).val()).draw() ;
           });
           $(".dataTables_filter").hide();
           oTable.on('order.dt search.dt', function(){
             oTable.column(0, {search:'applied', order:'applied'}).nodes().each(
              function (cell, i) {
               cell.innerHTML = i+1;
               } );
           }).draw();
         });
         function allowDrop(event) {
         event.preventDefault();
         }
         function drag(event, id) {
             var img1 = document.getElementById(id);
             event.dataTransfer.setData("osaicon", img1.id);
             img1.style.opacity = 0;
         }
         function drop(event) {
             var data = event.dataTransfer.getData("osaicon");
            
             if ( event.target.nodeName == "IMG" || event.target.nodeName == "BUTTON" || event.target.nodeName == "A") {
             }else{
               event.target.appendChild(document.getElementById(data));
             }
         }

         function show(id) {
             var img1 = document.getElementById(id);
             img1.style.opacity = 1;
         }

